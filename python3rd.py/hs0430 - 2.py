print("x, y, z 3개의 값을 입력하시오: ") #"x, y, z 3개의 값을 입력하시오 : " 를 출력
x = int(input("x의 값을 입력: ")) #변수 x를 숫자형 ("x의 값을 입력 ") 으로 선언
y = int(input("y의 값을 입력: ")) #변수 y를 숫자형 ("y의 값을 입력 ") 으로 선언
z = int(input("z의 값을 입력: ")) #변수 z를 숫자형 ("z의 값을 입력 ") 으로 선언
x = x + 1 #변수 x를 x와 1을 더한 값으로 출력
y = y + 1 #변수 y를 y와 1을 더한 값으로 출력
z = z + 1 #변수 z를 z와 1을 더한 값으로 출력
print("변경된 값은:", x, y, z) #"변경된 값은: x, y, z" 를 출력

#결론 : 터미널에다 숫자를 입력하면 x, y, z에 1을 합친 값이 출력된다.
