a = "안녕" #변수 a (문자형 "안녕" 으로 선언)
b = "잘 지내니?" #변수 b (문자형 "잘 지내니?" 으로 선언)
c = "너 몇살이니?" #변수 c (문자형 "너 몇살이니?" 으로 선언)
d = "난" #변수 d (문자형 "난" 으로 선언)
e = 4 #변수 e (숫자형 4 으로 선언)
f = "살이야" #변수 f (문자형 "살이야" 으로 선언)

print("안녕"+"잘 지내니?") #안녕 잘 지내니 출력
print("너 몇살이니?"+"난"+str(4)+"살이야") #너 몇살이니? 난 4살이야 출력
print(a+b) #문자형 a와 b를 더한 값 출력
print(c+d+str(e)+f) #문자형 c와 d와 e와 f를 더한 값 출력

#결론 : 문자형과 숫자형을 같이 쓸 수 없으니 둘 중 하나를 같은 형으로 만들어야 된다.