print("hello world") #hello world 출력
print("안녕 파이썬") #안녕 파이썬 출력

x = "안녕" #변수 x (문자형 "안녕" 으로 선언)
y = "파이썬" #변수 y (문자형 "파이썬" 으로 선언)
z = x + y #z = 문자형 x와 문자형 y를 더한 값
print(x) #문자형 x 출력
print(y) #문자형 y 출력
print(x+y) #문자형 x와 문자형 y를 더한 값 출력
print(z) #문자형 z 출력 (문자형 x와 문자형 y를 더한 값 출력)

#결론 : 문자형은 쓸 때 "" 을 붙여야한다, z = x+y 로 간추리면 효과가 좋다