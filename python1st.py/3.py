a = "안녕" #변수 a (문자형 "안녕" 으로 선언)
b = "넌 누구니" #변수 b (문자형 "넌 누구니" 으로 선언)
c = "난 새내기야" #변수 b (문자형 "난 새내기야" 으로 선언)
d = a+b #d = 문자형 a와 문자형 b를 더한 값
e = a+b+c #e = 문자형 a와 문자형 b, 문자형 c를 더한 값

print(a) #문자형 a 출력
print(b) #문자형 b 출력
print(c) #문자형 c 출력
print(d) #문자형 d 출력
print(e) #문자형 e 출력

